package com.atwebpages.awaillixa.merofirebase;

public class DAO {

    private String caption;

    private String description;

    private String imageUrl;

    private String postIdUploader;

    private String postId;

    public String getCaption() {
        return caption;
    }

    public void setCaption(String caption) {
        this.caption = caption;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }


    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }

    public String getPostIdUploader() {
        return postIdUploader;
    }

    public void setPostIdUploader(String postIdUploader) {
        this.postIdUploader = postIdUploader;
    }

    public String getPostId() {
        return postId;
    }

    public void setPostId(String postId) {
        this.postId = postId;
    }
}

